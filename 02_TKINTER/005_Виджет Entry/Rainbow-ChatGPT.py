# Написать программу на python и tkinter,
# состоящую из семи кнопок, цвета которых соответствуют цветам радуги.
# При нажатии на ту или иную кнопку в текстовое поле должен вставляться код цвета,
# а в метку – название цвета.
#
# Цвета радуги должны быть следующие:
# #ff0000: Красный
# #ff7d00: Оранжевый
# #ffff00: Желтый
# #00ff00: Зеленый
# #007dff: Голубой
# #0000ff: Синий
# #7d00ff: Фиолетовый
######################################################################################

import tkinter as tk

# Словарь цветов радуги: код цвета и название
colors = {
    "#ff0000": "Красный",
    "#ff7d00": "Оранжевый",
    "#ffff00": "Желтый",
    "#00ff00": "Зеленый",
    "#007dff": "Голубой",
    "#0000ff": "Синий",
    "#7d00ff": "Фиолетовый"
}

#-----------------------------------
# Функция обработки нажатия кнопки
#-----------------------------------
def on_color_button_click(color_code):
    color_name = colors[color_code] # название цвета
    color_label.config(text=color_name) # установить цвет
    color_entry.delete(0, tk.END)   # удалить (поле ввода)
    color_entry.insert(0, color_code)   # вставить (поле ввода)

# Основное окно
root = tk.Tk()
root.title("Цвета радуги")

# Метка для отображения названия цвета
color_label = tk.Label(root, text="Выберите цвет", font=("Arial", 14))  # объект label
color_label.pack(pady=10)   # локализация

# Текстовое поле для вывода кода цвета
color_entry = tk.Entry(root, font=("Arial", 14), justify="center")  # объект "поле ввода"
color_entry.pack(pady=10)   # локализация

# Рамка для кнопок
button_frame = tk.Frame(root)
button_frame.pack(pady=10)

# Создание кнопок по цветам
for color_code in colors:   # сканирование словаря цветов (color[]) по ключам для получения кода цвета (color_code)
    btn = tk.Button(
        button_frame,
        bg=color_code,
        width=10,
        height=2,
        command=lambda c=color_code: on_color_button_click(c)   # action click
    )
    btn.pack(side=tk.LEFT, padx=5)

# Запуск главного цикла
root.mainloop()

#-----------------------------------------------------------------
#  - Показывает метку и текстовое поле.
#  - Создаёт 7 кнопок в одну строку, каждая с цветом радуги.
#
#  При нажатии на кнопку:
#   - В метке отображается название цвета.
#   - В текстовом поле появляется соответствующий HEX-код цвета.
