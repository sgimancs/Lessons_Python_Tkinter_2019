###########################################
# CLASS
###########################################
class A:
    # Свойства
    property1 = 'Property 1'
    property2 = 'Property 2'
    name = 'guest'

    # Метод
    # Обязательный self - указатель на экземпляр класса (т.к. экземпляров может быть несколько)
    def say_hi(self, name=''):
        if name:
            return 'Hi, ' + name
        else:
            return 'Hello, ' + self.name


########################################
# OBJECT
########################################
# Создать объект класса
a = A()     # 1-й экземпляр класса A()
b = A()     # 2-й экземпляр класса A()

# Создать свойства "на лету" (для "пуcтого" класса) - НЕ РЕКОМЕНДУЕТСЯ!
# a.property1 = 'Property 1'
# a.property2 = 'Property 2'
# print(a)

print('a.property1:', a.property1)
