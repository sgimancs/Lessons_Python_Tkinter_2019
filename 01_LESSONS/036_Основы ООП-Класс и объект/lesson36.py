# Класс описывает устройство и поведение объекта
# Объект - это экземпляр класса (созданный по схеме описанной в классе)
# Объекты имеют свои свойства (атрибуты) и методы (функции)
# Все типы данных - это объекты
# Функция описанная в классе наз-ся методом
# Свойства класса - это переменные
#

