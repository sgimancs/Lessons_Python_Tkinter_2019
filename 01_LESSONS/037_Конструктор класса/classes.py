# self - обязательный указатель на экземпляр класса (объект)

class Person:

    # КОНСТРУКТОР (инициализация класса)
    def __init__(self, name):   # 1-й параметр "self" - обязателен
        self.name = name

    # МЕТОД
    def print_info(self):
        print(f'Hello, my name is {self.name}')
